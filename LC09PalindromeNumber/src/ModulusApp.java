
public class ModulusApp {

	public static void main(String[] args) {
		System.out.println("10%3 = "+10%3);// 10/3=1 where remainder = 1
		System.out.println("15%4 = "+15%4);// 15/4=3 where remainder = 3
		System.out.println("7%2 = "+7%2);// 10/3=3 where remainder = 1

		System.out.println("123456%10 = "+123456%10);

	}

}
